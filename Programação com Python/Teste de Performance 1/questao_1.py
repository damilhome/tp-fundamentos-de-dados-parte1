'''
Crie um programa que peça ao usuário para inserir dois números e, em seguida, calcule e exiba a 
soma, subtração, multiplicação, divisão e divisão inteira desses números.
'''

num_1 = int(input('Insira um número: '))
num_2 = int(input('Insira outro número: '))

print(f'{num_1} + {num_2} = {num_1 + num_2}')
print(f'{num_1} - {num_2} = {num_1 - num_2}')
print(f'{num_1} * {num_2} = {num_1 * num_2}')
print(f'{num_1} / {num_2} = {num_1 / num_2}')
print(f'{num_1} // {num_2} = {num_1 // num_2}')